def generate_ai_analysis_pdf(
    ai_response: str,
    chat_history: list,
    specs: dict,
    results: dict,
    company_name: str = "",
    author_name: str = "",
    logo_path: str = "",
    output_path: str = "pv_ai_analysis.pdf"
) -> str:
    """Generate PDF of AI analysis and chat"""
    
    pdf = SunnysidePDF(company_name=company_name, author_name=author_name, logo_path=logo_path)
    
    # Cover
    pdf.add_page()
    pdf.set_y(50)
    pdf.set_font(pdf.ufont_bold, '', 24)
    pdf.set_text_color(0, 212, 255)
    pdf.cell(0, 12, 'AI-POWERED ANALYSIS', 0, 1, 'C')
    pdf.set_font(pdf.ufont_bold, '', 20)
    pdf.set_text_color(30, 30, 30)
    pdf.cell(0, 10, 'Sunnyside AI Advisory Report', 0, 1, 'C')
    pdf.ln(5)
    pdf.set_font(pdf.ufont, '', 10)
    pdf.set_text_color(100, 100, 100)
    pdf.cell(0, 6, f"Project: {specs.get('name', 'N/A')}", 0, 1, 'C')
    pdf.cell(0, 6, f"Generated: {datetime.now().strftime('%B %d, %Y at %H:%M')}", 0, 1, 'C')
    
    # AI Overview
    pdf.add_page()
    pdf.chapter_title("Executive AI Assessment", "🤖")
    
    pdf.set_font(pdf.ufont_bold, '', 11)
    pdf.set_text_color(0, 212, 255)
    pdf.cell(0, 8, 'System Overview', 0, 1)
    pdf.ln(1)
    
    pdf.info_row('Location', specs.get('name', 'N/A'))
    pdf.info_row('System Capacity', f"{results['dc_capacity_kw']:.1f}", "kW")
    pdf.info_row('Annual Production', f"{results['annual_kwh']:,.0f}", "kWh")
    pdf.info_row('Performance Ratio', f"{results['performance_ratio']:.1f}", "%")
    
    pdf.ln(5)
    pdf.set_font(pdf.ufont_bold, '', 11)
    pdf.set_text_color(0, 212, 255)
    pdf.cell(0, 8, 'AI Recommendations', 0, 1)
    pdf.ln(1)
    
    # AI response body
    pdf.set_font(pdf.ufont, '', 10)
    pdf.set_text_color(50, 50, 50)
    
    # Clean markdown-like formatting from AI response for PDF
    clean_response = ai_response.replace('**', '').replace('*', '').replace('#', '')
    pdf.multi_cell(0, 6, clean_response)
    
    pdf.ln(5)
    pdf.set_draw_color(0, 212, 255)
    pdf.set_line_width(0.3)
    pdf.line(15, pdf.get_y(), 195, pdf.get_y())
    pdf.ln(3)
    pdf.set_font(pdf.ufont_italic, '', 8)
    pdf.set_text_color(150, 150, 150)
    pdf.cell(0, 5, "Analysis generated by Sunnyside AI using multi-agent LLM architecture.", 0, 1)
    
    # Chat history if available
    if chat_history and len(chat_history) > 1:
        pdf.add_page()
        pdf.chapter_title("Consultation Dialogue", "💬")
        
        for msg in chat_history:
            role = msg.get('role', '')
            content = msg.get('content', '')
            
            if role == 'user':
                pdf.set_font(pdf.ufont_bold, '', 10)
                pdf.set_text_color(59, 130, 246)
                pdf.cell(0, 7, 'User Question:', 0, 1)
                pdf.set_font(pdf.ufont, '', 9)
                pdf.set_text_color(60, 60, 60)
                pdf.multi_cell(0, 5, content.replace('**', ''))
                pdf.ln(2)
            elif role == 'assistant':
                pdf.set_font(pdf.ufont_bold, '', 10)
                pdf.set_text_color(245, 158, 11)
                pdf.cell(0, 7, 'Sunnyside AI:', 0, 1)
                pdf.set_font(pdf.ufont, '', 9)
                pdf.set_text_color(50, 50, 50)
                pdf.multi_cell(0, 5, content.replace('**', ''))
                pdf.ln(4)
                pdf.set_draw_color(230, 230, 230)
                pdf.line(15, pdf.get_y(), 195, pdf.get_y())
                pdf.ln(3)
    
    pdf.output(output_path)
    return output_path