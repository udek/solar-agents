class SunnysidePDF(FPDF):
    """Custom PDF class with professional solar report styling - Unicode support"""

    def __init__(self, company_name: str = "", author_name: str = "", logo_path: str = ""):
        super().__init__(orientation='P', unit='mm', format='A4')
        self.company_name = company_name
        self.author_name = author_name
        self.logo_path = logo_path
        self.set_auto_page_break(auto=True, margin=20)
        self.set_margins(15, 15, 15)
        # Unicode fonts
        self.ufont = "DejaVuSans"
        self.ufont_bold = "DejaVuSans-Bold"
        self.ufont_italic = "DejaVuSans-Oblique"
        # Register fonts with fallback logic
        import warnings
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            # Find DejaVu font files dynamically
            _FONT_DIRS = [
                '/usr/share/fonts/truetype/dejavu',
                '/usr/share/fonts/TTF',
                '/usr/share/fonts/truetype',
                '/usr/local/share/fonts',
            ]

            def find_font(name):
                for d in _FONT_DIRS:
                    path = os.path.join(d, name)
                    if os.path.exists(path):
                        return path
                return None

            def add_font_fallback(alias, preferred, fallback):
                """Try preferred font file, fall back to fallback if missing."""
                path = find_font(preferred)
                if not path:
                    path = find_font(fallback)
                if path:
                    self.add_font(alias, '', path, uni=True)
                # If no font found, FPdf defaults to Helvetica

            add_font_fallback(self.ufont, 'DejaVuSans.ttf', 'DejaVuSans.ttf')
            add_font_fallback(self.ufont_bold, 'DejaVuSans-Bold.ttf', 'DejaVuSans-Bold.ttf')
            add_font_fallback(self.ufont_italic, 'DejaVuSans-Oblique.ttf', 'DejaVuSansMono-Oblique.ttf')

    def header(self):
        # Gradient-like header bar
        self.set_fill_color(0, 20, 40)
        self.rect(0, 0, 210, 12, 'F')
        # Top accent line
        self.set_fill_color(0, 212, 255)
        self.rect(0, 12, 210, 1.5, 'F')
        # Logo
        if self.logo_path and os.path.exists(self.logo_path):
            try:
                self.image(self.logo_path, x=15, y=2, h=8)
            except Exception:
                pass
        # Company name
        self.set_xy(45 if (self.logo_path and os.path.exists(self.logo_path)) else 15, 3)
        self.set_font(self.ufont_bold, '', 10)
        self.set_text_color(255, 255, 255)
        if self.company_name:
            self.cell(0, 5, self.company_name, ln=0)
        # Right: app name
        self.set_xy(-80, 3)
        self.set_font(self.ufont, '', 9)
        self.set_text_color(0, 212, 255)
        self.cell(0, 5, 'Sunnyside AI | PV Simulator', ln=0, align='R')

    def footer(self):
        self.set_y(-20)
        self.set_fill_color(0, 212, 255)
        self.rect(0, self.get_y(), 210, 0.5, 'F')
        self.set_y(-15)
        self.set_font(self.ufont_italic, '', 8)
        self.set_text_color(120, 120, 120)
        left_text = f"Generated by Sunnyside AI on {datetime.now().strftime('%Y-%m-%d %H:%M')}"
        if self.author_name:
            left_text += f"  |  Prepared by: {self.author_name}"
        self.cell(0, 5, left_text, 0, 0, 'L')
        self.cell(0, 5, f'Page {self.page_no()}', 0, 0, 'R')

    def chapter_title(self, title: str, icon: str = ""):
        self.set_font(self.ufont_bold, '', 14)
        self.set_text_color(0, 212, 255)
        self.cell(0, 10, f"{icon}  {title}" if icon else title, ln=True)
        self.set_draw_color(0, 212, 255)
        self.line(15, self.get_y(), 195, self.get_y())
        self.ln(3)

    def chapter_body(self, text: str):
        self.set_font(self.ufont, '', 10)
        self.set_text_color(60, 60, 60)
        self.multi_cell(0, 6, text)
        self.ln(2)

    def info_row(self, label: str, value: str, unit: str = ""):
        self.set_font(self.ufont_bold, '', 10)
        self.set_text_color(80, 80, 80)
        self.cell(70, 6, f"{label}:", 0, 0)
        self.set_font(self.ufont, '', 10)
        self.set_text_color(0, 0, 0)
        display = f"{value} {unit}" if unit else str(value)
        self.cell(0, 6, display, 0, 1)

    def metric_box(self, label: str, value: str, unit: str = "", color: tuple = (0, 212, 255)):
        x = self.get_x()
        y = self.get_y()
        w = 55
        h = 28
        
        # Box background
        self.set_fill_color(245, 248, 250)
        self.rounded_rect(x, y, w, h, 3, 'F')
        
        # Accent bar
        self.set_fill_color(*color)
        self.rect(x, y, 3, h, 'F')
        
        # Label
        self.set_xy(x + 6, y + 4)
        self.set_font(self.ufont, '', 8)
        self.set_text_color(120, 120, 120)
        self.cell(w - 10, 5, label, 0, 2)
        
        # Value
        self.set_font(self.ufont_bold, '', 14)
        self.set_text_color(30, 30, 30)
        self.cell(w - 10, 8, f"{value} {unit}".strip(), 0, 0)
        
        self.set_xy(x + w + 5, y)

    def rounded_rect(self, x, y, w, h, r, style=''):
        """Draw a rounded rectangle"""
        self.set_draw_color(200, 200, 200)
        self.set_line_width(0.2)
        self.rect(x, y, w, h, style)


def create_monthly_chart(results: dict, width: int = 800, height: int = 350) -> str:
    """Create monthly production chart image"""
    months = list(results['monthly_kwh'].keys())
    values = list(results['monthly_kwh'].values())
    
    fig, ax = plt.subplots(figsize=(width / 100, height / 100), dpi=100)
    fig.patch.set_facecolor('#ffffff')
    ax.set_facecolor('#fafbfc')
    
    bars = ax.bar(months, values, color='#f59e0b', edgecolor='#d97706', linewidth=1.2, alpha=0.9)
    
    # Add value labels on top
    for bar in bars:
        height_bar = bar.get_height()
        ax.text(bar.get_x() + bar.get_width()/2., height_bar,
                f'{int(height_bar):,}',
                ha='center', va='bottom', fontsize=7, color='#4b5563')
    
    ax.set_xlabel('Month', fontsize=9, color='#374151')
    ax.set_ylabel('Energy (kWh)', fontsize=9, color='#374151')
    ax.set_title('Monthly Energy Production', fontsize=11, fontweight='bold', color='#111827', pad=10)
    ax.tick_params(colors='#6b7280', labelsize=8)
    ax.spines['top'].set_visible(False)
    ax.spines['right'].set_visible(False)
    ax.spines['left'].set_color('#e5e7eb')
    ax.spines['bottom'].set_color('#e5e7eb')
    ax.yaxis.grid(True, linestyle='--', alpha=0.4)
    
    plt.tight_layout()
    
    tmp_path = tempfile.mktemp(suffix='.png')
    fig.savefig(tmp_path, dpi=100, bbox_inches='tight', facecolor='white')
    plt.close(fig)
    return tmp_path


